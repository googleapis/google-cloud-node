  close(): Promise<void> {
    if (this.gkeHubStub && !this._terminated) {
      return this.gkeHubStub.then(stub => {
        this._terminated = true;
        stub.close();
        this.operationsClient.close();
      });
    }
    return Promise.resolve();
  }
